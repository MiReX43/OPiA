#define _CRT_SECURE_NO_WARNINGS 
#define _USE_MATH_DEFINES
#include <math.h>
#include <stdio.h>
#include <locale.h>
#include <stdlib.h>

#define MAX_CARS 10

struct Автомобиль
{
    char марка[50];
    char производитель[50];
    char тип[50];
    int год_выпуска;
    int год_регистрации;
};

void ввод_автомобиля(struct Автомобиль* автомобиль) {
    printf("Введите марку автомобиля: ");
    scanf("%49s", автомобиль->марка);
    printf("Введите производителя автомобиля: ");
    scanf("%49s", автомобиль->производитель);
    printf("Введите тип автомобиля: ");
    scanf("%49s", автомобиль->тип);
    printf("Введите год выпуска автомобиля: ");
    scanf("%d", &автомобиль->год_выпуска);
    printf("Введите год регистрации автомобиля: ");
    scanf("%d", &автомобиль->год_регистрации);
}

void вывод_автомобиля(struct Автомобиль автомобиль) {
    printf("Марка: %s\n", автомобиль.марка);
    printf("Производитель: %s\n", автомобиль.производитель);
    printf("Тип: %s\n", автомобиль.тип);
    printf("Год выпуска: %d\n", автомобиль.год_выпуска);
    printf("Год регистрации: %d\n", автомобиль.год_регистрации);
    printf("\n");
}

int main()
{
    setlocale(LC_CTYPE, "RUS");

    struct Автомобиль автомобили[MAX_CARS];
    int n;

    // Ввод количества автомобилей
    printf("Введите количество автомобилей (от 5 до 10): ");
    scanf("%d", &n);
    if (n < 5 || n > 10) {
        printf("Ошибка: Введите количество от 5 до 10.\n");
        return 1;
    }

    // Ввод данных для каждого автомобиля
    for (int i = 0; i < n; i++) {
        printf("\nАвтомобиль %d:\n", i + 1);
        ввод_автомобиля(&автомобили[i]);
    }

    // Поиск и вывод автомобилей марки "Toyota" с годом регистрации до 2007
    printf("\nАвтомобили марки 'Toyota', зарегистрированные до 2007 года:\n");
    int найдено = 0;
    for (int i = 0; i < n; i++) {
        if (strcmp(автомобили[i].марка, "Toyota") == 0 && автомобили[i].год_регистрации < 2007) {
            вывод_автомобиля(автомобили[i]);
            найдено = 1;
        }
    }

    if (!найдено) {
        printf("Нет автомобилей, соответствующих критериям.\n");
    }

    return 0;

}